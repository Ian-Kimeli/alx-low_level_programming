#include <stdio.h>

/**
 * div - divideds two integers
 * @a: first int
 * @b: second int
 *
 * Return: divided result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mul - multiplies two integers
 * @a: first int
 * @b: second int
 *
 * Return: addition
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * sub - subtract two integers
 * @a: first int
 * @b: second int
 *
 * Return: subtracted result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * add - add two integers
 * @a: first int
 * @b: second int
 *
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * mod - mod two integers
 * @a: first int
 * @b: second int
 *
 * Return: final answer
 */
int mod(int a, int b)
{
	return (a % b);
}
